s = 'my name is Niteen'

print(s[:])  # my name is Niteen
print(s[1:])  # y name is Niteen
print(s[:4])  # my n
print(s[4:6])  # am

print(s[::])  # my name is Niteen
print(s[::1])  # my name is Niteen
print(s[::2])  # m aei ien
print(s[::3])  # mnesie
print(s[::-1])  # neetiN si eman ym

print(s[-5:-2])  # ite
print(s[3:-2])  # name is Nite
